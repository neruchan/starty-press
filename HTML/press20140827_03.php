全国でエステティックサロン「ラ・パルレ」を展開する、株式会社ビューティーパートナーズ（本社：東京都文京区、代表取締役社長：山下昌範）は、水素のパワーで“サビない身体”をつくる抗酸化サプリメント「コーラル水素100」（150粒入り、税込15,120円）を2014年3月10日に発売いたしました。
今回リリースする商品は、活性酸素と結びつき水となって体外へ排出する高い抗酸化力と還元力を持っています。
<br>
<br>


<h3>■老化や病気の原因の約９０％とされる「悪玉活性酸素」を体外へ排出</h3>
人は酸素を吸って生きている限り、身体がどんどんサビていきます。<br>
そのサビをとってくれるのが【水素】です。細胞や遺伝子を傷つける活性酸素と結びつき、<br>
水となって体外へ排出する、高い【抗酸化力】と、酸化を中和する【還元力】を持っています。<br>
本製品は、このマルチな物質【水素】を大量に含むため、サビない細胞をつくります。


<br>
<br>


<h3>■８倍の還元力！腸内環境を整えて“綺麗”を実感</h3>
サンゴには無数の小さな穴が開いているため、大量の【活性水素】を閉じ込められます。<br>
本製品は、その穴に100%ピュアな水素を閉じ込めた焼成サンゴパウダーを使用。<br>
水素水では還元作用が約１時間しか持続しないところを、約７～８時間持続させることに成功。<br>
活性水素【抗酸化力】は、コエンザイムＱ１０の８６３倍を実現しました。<br>
またサンゴのミネラルで、水素放出後も腸内の余分な脂質・老廃物を体外へ排出させます。

<br>
<br>


<h3>■悪玉活性酸素が身体に溜まると、起こりうる不調</h3>
【肥満】糖分を分解する細胞を傷つけるため、脂肪が蓄積して肥満の原因に。<br>
【便秘】腸壁の細胞を傷つけ、血流悪化で老廃物の排せつがうまくいかず腸内環境悪化に。<br>
【むくみ】さらに身体の余分な水分や老廃物が皮膚の下に溜まり、むくみやすい状態に。

<br>
<br>


<h3>■「コーラル水素100」を食べて期待できる変化</h3>
むくみ、顔のたるみ、ダイエット、ニキビ、ニキビ跡、アレルギー、アトピー、頭痛、肩こり、<br>
白髪、薄毛、疲れ、不眠症、婦人病、糖尿病、ペットの健康などへの効果が期待できます。<br>
※効果には個人差があります。


